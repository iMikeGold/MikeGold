#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(scriptDirectory, "..");
const recordsRoot = join(projectRoot, "records");
const generatedDirectory = join(projectRoot, "src", "system", "generated");

function readCollection(directory) {
  const path = join(recordsRoot, directory);
  if (!existsSync(path)) return [];
  return readdirSync(path)
    .filter((file) => file.endsWith(".json"))
    .sort()
    .map((file) => JSON.parse(readFileSync(join(path, file), "utf8")));
}

function writeGenerated(name, typeName, exportName, records, serverOnly) {
  writeFileSync(
    join(generatedDirectory, name),
    `// Generated by scripts/generate-record-indexes.mjs. Do not edit.\n` +
      (serverOnly ? `import "server-only";\n\n` : "") +
      `import type { ${typeName} } from "${typeImports[typeName]}";\n\n` +
      `export const ${exportName} = ${JSON.stringify(records, null, 2)} as ${typeName}[];\n`,
  );
}

const typeImports = {
  InternalHatRecord: "@/system/hats/hat.types",
  PublicHat: "@/system/hats/hat.types",
  InternalProjectRecord: "@/system/projects/project.types",
  PublicProjectProjection: "@/system/projects/project.types",
  InternalWorkRecord: "@/system/work/work.types",
  PublicWorkProjection: "@/system/work/work.types",
  InternalEvidenceRecord: "@/system/evidence/evidence.types",
  PublicEvidenceProjection: "@/system/evidence/evidence.types",
  InternalRelationshipRecord: "@/system/relationships/relationship.types",
};

const hats = readCollection("hats");
const projects = readCollection("projects");
const work = readCollection("work");
const evidence = readCollection("evidence");
const relationships = readCollection("relationships");
const allRecords = [...hats, ...projects, ...work, ...evidence, ...relationships];
const byId = new Map(allRecords.map((record) => [record.id, record]));

const publicHats = hats
  .filter((record) => record.status === "published")
  .map((record) => ({
    id: record.legacyKey,
    slug: record.slug,
    name: record.name,
    category: record.category,
    type: record.type,
    ...(record.description ? { description: record.description } : {}),
    tags: record.tags,
    ...(record.weight ? { weight: record.weight } : {}),
    ...(record.relationships?.length
      ? {
          relationships: record.relationships.map((relationship) => {
            const target = byId.get(relationship.targetId);
            if (!target || target.recordType !== "hat") {
              throw new Error(`Hat ${record.slug} has an unresolved relationship.`);
            }
            return {
              targetId: target.slug,
              type: relationship.type,
              strength: relationship.strength,
            };
          }),
        }
      : {}),
    ...(record.details ? { details: record.details } : {}),
  }));

const publicProjects = projects
  .filter((record) => record.visibility === "public")
  .map((record) => ({
    slug: record.slug,
    name: record.name,
    summary: record.summary,
    status: record.status,
    featured: record.featured,
    ...(record.context ? { context: record.context } : {}),
    ...(record.establishedYear ? { establishedYear: record.establishedYear } : {}),
    ...(record.workStarted || record.workEnded
      ? {
          workPeriod: record.workEnded
            ? `${record.workStarted ?? "Unknown"}–${record.workEnded}`
            : `${record.workStarted}–present`,
        }
      : {}),
  }))
  .sort(
    (left, right) =>
      Number(right.featured) - Number(left.featured) ||
      left.name.localeCompare(right.name),
  );

const publicWork = work
  .filter((record) => record.visibility === "public")
  .map((record) => {
    const project = byId.get(record.projectId);
    if (!project || project.recordType !== "project" || project.visibility !== "public") {
      throw new Error(`Public Work ${record.slug} has no public Project.`);
    }
    const appliedHatSlugs = relationships
      .filter(
        (relationship) =>
          relationship.relationshipType === "applied-in" &&
          relationship.targetId === record.id,
      )
      .map((relationship) => byId.get(relationship.sourceId))
      .filter((hat) => hat?.recordType === "hat" && hat.status === "published")
      .map((hat) => hat.slug);
    const evidenceSlugs = relationships
      .filter(
        (relationship) =>
          relationship.relationshipType === "evidenced-by" &&
          relationship.sourceId === record.id,
      )
      .map((relationship) => byId.get(relationship.targetId))
      .filter(
        (item) => item?.recordType === "evidence" && item.visibility === "public",
      )
      .map((item) => item.slug);
    return {
      slug: record.slug,
      projectSlug: project.slug,
      title: record.title,
      summary: record.summary,
      status: record.status,
      ...(record.sequence ? { sequence: record.sequence } : {}),
      capabilityGroupIds: record.capabilityGroupIds ?? [],
      ...(record.stages ? { stages: record.stages } : {}),
      appliedHatSlugs,
      evidenceSlugs,
    };
  });

const publicEvidence = evidence
  .filter((record) => record.visibility === "public")
  .map((record) => ({
    slug: record.slug,
    title: record.title,
    ...(record.description ? { description: record.description } : {}),
    evidenceType: record.evidenceType,
    ...(record.role ? { role: record.role } : {}),
    ...(Number.isFinite(record.sequence) ? { sequence: record.sequence } : {}),
    ...(Number.isFinite(record.previewSequence) ? { previewSequence: record.previewSequence } : {}),
    ...(record.phase ? { phase: record.phase } : {}),
    ...(record.period ? { period: record.period } : {}),
    ...(record.assetPath ? { assetPath: record.assetPath } : {}),
    ...(record.externalUrl ? { externalUrl: record.externalUrl } : {}),
    ...(record.thumbnailUrl ? { thumbnailUrl: record.thumbnailUrl } : {}),
    ...(record.sourceTitle ? { sourceTitle: record.sourceTitle } : {}),
    ...(record.sourceAuthor ? { sourceAuthor: record.sourceAuthor } : {}),
    placeholder: record.placeholder,
  }));

mkdirSync(generatedDirectory, { recursive: true });

writeGenerated(
  "internal-hat-records.generated.ts",
  "InternalHatRecord",
  "internalHatRecords",
  hats,
  true,
);
writeGenerated(
  "public-hats.generated.ts",
  "PublicHat",
  "publicHats",
  publicHats,
  false,
);
writeGenerated(
  "internal-project-records.generated.ts",
  "InternalProjectRecord",
  "internalProjectRecords",
  projects,
  true,
);
writeGenerated(
  "public-projects.generated.ts",
  "PublicProjectProjection",
  "publicProjects",
  publicProjects,
  false,
);
writeGenerated(
  "internal-work-records.generated.ts",
  "InternalWorkRecord",
  "internalWorkRecords",
  work,
  true,
);
writeGenerated(
  "public-work.generated.ts",
  "PublicWorkProjection",
  "publicWork",
  publicWork,
  false,
);
writeGenerated(
  "internal-evidence-records.generated.ts",
  "InternalEvidenceRecord",
  "internalEvidenceRecords",
  evidence,
  true,
);
writeGenerated(
  "public-evidence.generated.ts",
  "PublicEvidenceProjection",
  "publicEvidence",
  publicEvidence,
  false,
);
writeGenerated(
  "internal-relationship-records.generated.ts",
  "InternalRelationshipRecord",
  "internalRelationshipRecords",
  relationships,
  true,
);

const publicSerialization = JSON.stringify([
  publicHats,
  publicProjects,
  publicWork,
  publicEvidence,
]);
for (const record of allRecords) {
  if (publicSerialization.includes(record.id)) {
    throw new Error(`Public projection leaked internal UUID for ${record.slug ?? record.id}.`);
  }
}

console.log(
  JSON.stringify(
    {
      internal: {
        hats: hats.length,
        projects: projects.length,
        work: work.length,
        evidence: evidence.length,
        relationships: relationships.length,
      },
      public: {
        hats: publicHats.length,
        projects: publicProjects.length,
        work: publicWork.length,
        evidence: publicEvidence.length,
      },
      uuidLeaks: 0,
    },
    null,
    2,
  ),
);
