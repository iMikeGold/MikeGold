#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(scriptDirectory, "..");
const recordsRoot = join(projectRoot, "records");
const generatedDirectory = join(projectRoot, "src", "system", "generated");
const serviceIndexDirectory = join(recordsRoot, "generated", "service-engine");
mkdirSync(serviceIndexDirectory, { recursive: true });

function readCollection(directory) {
  const path = join(recordsRoot, directory);
  if (!existsSync(path)) return [];
  return readdirSync(path)
    .filter((file) => file.endsWith(".json"))
    .sort()
    .map((file) => JSON.parse(readFileSync(join(path, file), "utf8")));
}

function writeGenerated(name, typeName, exportName, records, serverOnly) {
  writeFileSync(
    join(generatedDirectory, name),
    `// Generated by scripts/generate-record-indexes.mjs. Do not edit.\n` +
      (serverOnly ? `import "server-only";\n\n` : "") +
      `import type { ${typeName} } from "${typeImports[typeName]}";\n\n` +
      `export const ${exportName} = ${JSON.stringify(records, null, 2)} as ${typeName}[];\n`,
  );
}

