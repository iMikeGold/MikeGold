const hatSemanticProfiles = JSON.parse(readFileSync(join(recordsRoot, "concepts", "hat-semantic-profiles.json"), "utf8")).profiles;
const capabilityCoveragePolicy = JSON.parse(readFileSync(join(recordsRoot, "concepts", "capability-coverage-policy.json"), "utf8"));
const allRecords = [...hats, ...projects, ...work, ...evidence, ...relationships];
const byId = new Map(allRecords.map((record) => [record.id, record]));

const publicHats = hats
  .filter((record) => record.status === "published")
  .map((record) => ({
    id: record.legacyKey,
    slug: record.slug,
    name: record.name,
    category: record.category,
    type: record.type,
    ...(record.description ? { description: record.description } : {}),
    tags: record.tags,
    ...(record.weight ? { weight: record.weight } : {}),
    ...(record.relationships?.length
      ? {
          relationships: record.relationships.map((relationship) => {
            const target = byId.get(relationship.targetId);
            if (!target || target.recordType !== "hat") {
              throw new Error(`Hat ${record.slug} has an unresolved relationship.`);
            }
            return {
              targetId: target.slug,
              type: relationship.type,
              strength: relationship.strength,
            };
          }),
        }
      : {}),
    ...(record.details ? { details: record.details } : {}),
  }));

const publicProjects = projects
  .filter((record) => record.visibility === "public")
  .map((record) => ({
    slug: record.slug,
    name: record.name,
    summary: record.summary,
    status: record.status,
    featured: record.featured,
    ...(record.context ? { context: record.context } : {}),
    ...(record.establishedYear ? { establishedYear: record.establishedYear } : {}),
    ...(record.workStarted || record.workEnded
      ? {
          workPeriod: record.workEnded
            ? `${record.workStarted ?? "Unknown"}–${record.workEnded}`
            : `${record.workStarted}–present`,
        }
      : {}),
    ...(() => {
      const configuredPreferences = Object.entries(lensPriorityCalibration).flatMap(([lensId, slugs]) => {
        const editorialSequence = slugs.indexOf(record.slug);
        return editorialSequence < 0
          ? []
          : [{
              lensId,
              editorialSequence: editorialSequence + 1,
              editorialBoost: Math.max(2, 10 - editorialSequence),
              showcase: true,
              reason: "Curated lens priority calibrated against the shared editorial rubric.",
            }];
      });
      const preferences = [
        ...(record.lensPresentationPreferences ?? []).filter(
          (preference) =>
            !configuredPreferences.some((configured) => configured.lensId === preference.lensId),
        ),
        ...configuredPreferences,
      ];
      return preferences.length ? { lensPresentationPreferences: preferences } : {};
    })(),
  }))
  .sort(
    (left, right) =>
      Number(right.featured) - Number(left.featured) ||
      left.name.localeCompare(right.name),
  );

const publicWork = work
  .filter((record) => record.visibility === "public")
  .map((record) => {
    const project = byId.get(record.projectId);
    if (!project || project.recordType !== "project" || project.visibility !== "public") {
      throw new Error(`Public Work ${record.slug} has no public Project.`);
    }
    const relationshipHatSlugs = relationships
      .filter(
        (relationship) =>
          relationship.relationshipType === "applied-in" &&
          relationship.targetId === record.id,
      )
      .map((relationship) => byId.get(relationship.sourceId))
      .filter((hat) => hat?.recordType === "hat" && hat.status === "published")
      .map((hat) => hat.slug);
    const claimHatSlugs = serviceEngineClaims
      .filter((claim) => claim.workSlug === record.slug && ["authored", "confirmed"].includes(claim.status))
      .flatMap((claim) => claim.hatAssignments ?? [])
      .map((assignment) => assignment.hatSlug)
      .filter((slug) => publicHats.some((hat) => hat.slug === slug));
    const appliedHatSlugs = [...new Set([...relationshipHatSlugs, ...claimHatSlugs])];
    const evidenceRelationships = relationships
      .filter(
        (relationship) =>
          relationship.relationshipType === "evidenced-by" &&
          relationship.sourceId === record.id,
      )
      .flatMap((relationship) => {
        const item = byId.get(relationship.targetId);
        if (item?.recordType !== "evidence" || item.visibility !== "public") return [];
        return [{ relationship, item }];
      });
    const evidenceSlugs = evidenceRelationships.map(({ item }) => item.slug);
    return {
      slug: record.slug,
      projectSlug: project.slug,
      title: record.title,
      summary: record.summary,
      status: record.status,
      ...(record.sequence ? { sequence: record.sequence } : {}),
      capabilityGroupIds: record.lensAssignments.map((assignment) => assignment.lensId),
      lensAssignments: record.lensAssignments,
      ...(record.stages ? { stages: record.stages } : {}),
      appliedHatSlugs,
      evidenceSlugs,
      evidenceLinks: evidenceRelationships.map(({ relationship, item }) => ({
        evidenceSlug: item.slug,
        role: relationship.role ?? "supporting",
        ...(relationship.supportedLensIds?.length ? { supportedLensIds: relationship.supportedLensIds } : {}),
        ...(relationship.displayRoles?.length ? { displayRoles: relationship.displayRoles } : {}),
        ...(Number.isFinite(relationship.priority) ? { priority: relationship.priority } : {}),
      })),
      ...(() => {
        const lensSummaries = Object.fromEntries(
          record.lensAssignments
            .filter((assignment) => assignment.lensSummary)
            .map((assignment) => [assignment.lensId, assignment.lensSummary]),
        );
        return Object.keys(lensSummaries).length ? { lensSummaries } : {};
      })(),
    };
  });

const publicEvidence = evidence
  .filter((record) => record.visibility === "public")
  .map((record) => ({
    slug: record.slug,
    title: record.title,
    ...(record.description ? { description: record.description } : {}),
    evidenceType: record.evidenceType,
    ...(record.role ? { role: record.role } : {}),
    ...(Number.isFinite(record.sequence) ? { sequence: record.sequence } : {}),
    ...(Number.isFinite(record.previewSequence) ? { previewSequence: record.previewSequence } : {}),
    ...(record.phase ? { phase: record.phase } : {}),
    ...(record.period ? { period: record.period } : {}),
    ...(record.assetPath ? { assetPath: record.assetPath } : {}),
    ...(record.externalUrl ? { externalUrl: record.externalUrl } : {}),
    ...(record.thumbnailUrl ? { thumbnailUrl: record.thumbnailUrl } : {}),
    ...(!record.thumbnailUrl && record.evidenceType === "video" && youtubeThumbnail(record.externalUrl) ? { thumbnailUrl: youtubeThumbnail(record.externalUrl) } : {}),
    ...(record.presentation ? { presentation: record.presentation } : { presentation: { facets: inferredFacets(record), displayRoles: record.placeholder ? ["archive"] : ["lens-card", "supporting", "gallery"], visualQuality: record.role === "cover" ? "hero" : "standard", aspectPreference: "any" } }),
    ...(record.sourceTitle ? { sourceTitle: record.sourceTitle } : {}),
    ...(record.sourceAuthor ? { sourceAuthor: record.sourceAuthor } : {}),
    placeholder: record.placeholder,
  }));

const semanticConcepts = [...coreConceptKnowledge.concepts, ...extendedConceptKnowledge.concepts]
  .sort((left, right) => left.slug.localeCompare(right.slug));
const conceptBySlug = new Map(semanticConcepts.map((concept) => [concept.slug, concept]));
const normaliseSemanticText = (value) => ` ${value.toLowerCase().replaceAll(/[^a-z0-9]+/g, " ").trim()} `;
const normalisedPhrases = new Map(semanticConcepts.map((concept) => [
  concept.slug,
  concept.phrases.map((phrase) => ` ${normaliseSemanticText(phrase).trim()} `),
]));
const matchingConcepts = (text) => {
  const normalised = normaliseSemanticText(text);
  return semanticConcepts
    .filter((concept) => normalisedPhrases.get(concept.slug).some((phrase) => normalised.includes(phrase)))
    .map((concept) => concept.slug);
};
const expandConceptSlugs = (slugs) => [...new Set(slugs.flatMap((slug) => {
  const concept = conceptBySlug.get(slug);
  return [slug, ...(concept?.relatedConceptSlugs ?? []), ...(concept?.broaderConceptSlugs ?? [])];
}))].sort();
const confirmedClaimConceptsByWork = new Map();
for (const claim of serviceEngineClaims.filter((item) => item.status === "confirmed")) {
  const current = confirmedClaimConceptsByWork.get(claim.workSlug) ?? [];
  confirmedClaimConceptsByWork.set(
    claim.workSlug,
    [...new Set([...current, ...(claim.conceptSlugs ?? [])])].sort(),
  );
}
const workSemanticIndex = Object.fromEntries(publicWork.map((item) => {
  const authored = workSemanticProfiles[item.slug];
  const claimConcepts = confirmedClaimConceptsByWork.get(item.slug) ?? [];
  const text = `${item.title} ${item.summary} ${item.lensAssignments.map((assignment) => `${assignment.rationale} ${assignment.lensSummary ?? ""}`).join(" ")}`;
  const concepts = [...new Set([...(authored ?? matchingConcepts(text)), ...claimConcepts])].sort();
  return [item.slug, {
    projectSlug: item.projectSlug,
    concepts,
    source: claimConcepts.length ? "confirmed-claims" : authored ? "authored" : "derived",
  }];
}).sort(([left], [right]) => left.localeCompare(right)));
const hatSemanticIndex = Object.fromEntries(publicHats.map((hat) => {
  const profile = hatSemanticProfiles[hat.slug];
  const authoredCoverage = profile?.coverage;
  const authored = authoredCoverage
    ? [...authoredCoverage.direct, ...authoredCoverage.core, ...authoredCoverage.supporting, ...authoredCoverage.contextual]
    : undefined;
  const text = [
    hat.name, hat.type, hat.category, hat.description,
    ...hat.tags.core, ...hat.tags.adjacent, ...(hat.tags.meta ?? []),
    hat.details?.overview, ...(hat.details?.capabilities ?? []), ...(hat.details?.usedFor ?? []),
  ].filter(Boolean).join(" ");
  const indexedConceptSlugs = expandConceptSlugs(authored ?? matchingConcepts(text));
  return [hat.slug, {
    coverage: authoredCoverage ?? {
      direct: [],
      core: authored ? indexedConceptSlugs : [],
      supporting: [],
      contextual: authored ? [] : indexedConceptSlugs,
    },
    source: authored ? profile.source : "derived",
  }];
}).sort(([left], [right]) => left.localeCompare(right)));
const conceptToWorkIndex = Object.fromEntries(semanticConcepts.map((concept) => [
  concept.slug,
  Object.entries(workSemanticIndex)
    .filter(([, signature]) => signature.concepts.includes(concept.slug))
    .map(([workSlug]) => workSlug)
    .sort(),
]));
const hatToWorkIndex = Object.fromEntries(publicHats.map((hat) => [
  hat.slug,
  publicWork.filter((item) => item.appliedHatSlugs.includes(hat.slug)).map((item) => item.slug).sort(),
]));
const projectContributionIndex = Object.fromEntries(publicProjects.map((project) => [
  project.slug,
  publicWork.filter((item) => item.projectSlug === project.slug).map((item) => item.slug).sort(),
]));
const canonicalise = (value) => Array.isArray(value)
  ? value.map(canonicalise).sort((left, right) => JSON.stringify(left).localeCompare(JSON.stringify(right)))
  : value && typeof value === "object"
    ? Object.fromEntries(Object.entries(value).sort(([left], [right]) => left.localeCompare(right)).map(([key, item]) => [key, canonicalise(item)]))
    : value;
const contentHash = (value) => createHash("sha256").update(JSON.stringify(canonicalise(value))).digest("hex");
const archiveSourceHash = contentHash({
  hats,
  work,
  relationships,
  serviceEngineClaims,
  workSemanticProfiles,
  hatSemanticProfiles,
  capabilityCoveragePolicy,
  semanticConcepts,
});
const serviceIndexes = {
  workSemanticIndex,
  hatSemanticIndex,
  conceptToWorkIndex,
  hatToWorkIndex,
  projectContributionIndex,
};
const generatedIndexHash = contentHash(serviceIndexes);
for (const [file, data] of [
  ["work-semantic-index.json", { schemaVersion: 1, work: workSemanticIndex }],
  ["hat-semantic-index.json", { schemaVersion: 1, hats: hatSemanticIndex }],
  ["concept-to-work-index.json", { schemaVersion: 1, concepts: conceptToWorkIndex }],
  ["hat-to-work-index.json", { schemaVersion: 1, hats: hatToWorkIndex }],
  ["project-contribution-index.json", { schemaVersion: 1, projects: projectContributionIndex }],
  ["index-manifest.json", {
    schemaVersion: 1,
    generatorVersion: "service-index-generator/1.0",
    archiveSourceHash,
    generatedIndexHash,
  }],
]) writeFileSync(join(serviceIndexDirectory, file), `${JSON.stringify(data, null, 2)}\n`);

const publicEvidenceBySlug = new Map(publicEvidence.map((record) => [record.slug, record]));

function scoreEvidence(record, links, lensId) {
  if (!record || record.placeholder) return -Infinity;
  if (
    record.presentation?.displayRoles?.length === 1 &&
    record.presentation.displayRoles[0] === "archive"
  ) return -Infinity;
  const src = record.assetPath ?? record.thumbnailUrl;
  if (!src) return -Infinity;
  const facets = record.presentation?.facets ?? [];
  const preference = lensId ? lensFacetPriority[lensId] : ["project-overview", "website", "application-interface", "identity-system", "video", "photography"];
  const facetRank = Math.min(...facets.map((facet) => { const index = preference.indexOf(facet); return index < 0 ? 99 : index; }), 99);
  const matchingLinks = links.filter((link) => link.evidenceSlug === record.slug);
  const direct = matchingLinks.length ? 80 : 0;
  const lensSupport = matchingLinks.some((link) => link.supportedLensIds?.includes(lensId)) ? 35 : 0;
  const cardSuitable = record.presentation?.displayRoles?.includes("lens-card") ? 12 : 0;
  const quality = record.presentation?.visualQuality === "hero" ? 8 : record.presentation?.visualQuality === "standard" ? 4 : 0;
  const facetScore = facetRank === 99 ? 0 : Math.max(0, 45 - facetRank * 6);
  const override = matchingLinks.reduce((best, link) => Math.min(best, link.priority ?? 999), 999);
  return direct + lensSupport + cardSuitable + quality + facetScore - (override === 999 ? 0 : override / 1000);
}

const publicWorkCards = [];
for (const project of publicProjects) {
  const projectWork = publicWork.filter((item) => item.projectSlug === project.slug);
  const lenses = [...new Set(projectWork.flatMap((item) => item.capabilityGroupIds))];
  for (const lensId of [undefined, ...lenses]) {
    const relevantWork = lensId ? projectWork.filter((item) => item.capabilityGroupIds.includes(lensId)) : projectWork;
    if (!relevantWork.length) continue;
    const orderedWork = [...relevantWork].sort((left, right) => {
      const leftRole = left.lensAssignments.find((assignment) => assignment.lensId === lensId)?.role;
      const rightRole = right.lensAssignments.find((assignment) => assignment.lensId === lensId)?.role;
      return Number(rightRole === "primary") - Number(leftRole === "primary") ||
        (left.sequence ?? 999) - (right.sequence ?? 999) ||
        left.slug.localeCompare(right.slug);
    });
    const allLinks = relevantWork.flatMap((item) => item.evidenceLinks);
    const directEvidenceSlugs = new Set(allLinks.map((link) => link.evidenceSlug));
    const projectEvidenceSlugs = new Set(projectWork.flatMap((item) => item.evidenceSlugs));
    const candidates = [...new Set([...directEvidenceSlugs, ...projectEvidenceSlugs])]
      .map((slug) => publicEvidenceBySlug.get(slug))
      .filter(Boolean)
      .map((record) => ({ record, score: scoreEvidence(record, allLinks, lensId) }))
      .filter(({ score }) => Number.isFinite(score))
      .sort((left, right) => right.score - left.score || (left.record.sequence ?? 999) - (right.record.sequence ?? 999) || left.record.slug.localeCompare(right.record.slug));
    const visuals = candidates.slice(0, 3).map(({ record }) => ({ evidenceSlug: record.slug, src: record.assetPath ?? record.thumbnailUrl, alt: record.description ?? record.title, evidenceType: record.evidenceType }));
    const leadHatSlugs = [...new Set(orderedWork.flatMap((item) => item.appliedHatSlugs))];
    const summary = (lensId && orderedWork.find((item) => item.lensSummaries?.[lensId])?.lensSummaries?.[lensId]) ?? orderedWork[0]?.summary ?? project.summary;
    const weightedContributionCount = lensId
      ? orderedWork.reduce((total, item) => {
          const role = item.lensAssignments.find((assignment) => assignment.lensId === lensId)?.role;
          return total + (role === "primary" ? 1 : 0.45);
        }, 0)
      : orderedWork.length;
    const contributionScore = Math.min(62, 38 + Math.log2(weightedContributionCount + 1) * 12);
    const directlySupportedEvidence = allLinks.filter((link) => !lensId || link.supportedLensIds?.includes(lensId)).length;
    const evidenceCompleteness = Math.min(16, Math.sqrt(candidates.length) * 6) + (visuals[0] ? 10 : 0);
    const documentationDepth = Math.min(10, orderedWork.reduce((total, item) => total + (item.stages?.length ?? 0) * 2 + Number(Boolean(item.lensSummaries?.[lensId])), 0));
    const capabilityDistinctiveness = Math.min(8, Math.sqrt(leadHatSlugs.length) * 2);
    const directLensSupport = lensId && directlySupportedEvidence ? 8 : 0;
    const relevanceScore = Math.round((contributionScore + evidenceCompleteness + documentationDepth + capabilityDistinctiveness + directLensSupport) * 100) / 100;
    const preference = lensId ? project.lensPresentationPreferences?.find((item) => item.lensId === lensId) : undefined;
    const editorialBoost = Math.max(-10, Math.min(10, preference?.editorialBoost ?? 0));
    // Editorial relevance is a composite, not a fixed rank:
    // 25% evidence-derived system score, a bounded 12–60 editorial calibration,
    // and a small showcase-readiness signal. Unranked/new projects still enter
    // automatically through their evidence-derived score.
    const finalScore =
      relevanceScore * 0.25 +
      editorialBoost * 6 +
      (preference?.showcase ? 3 : 0);
    const relevanceReasons = [
      `${orderedWork.length} directly relevant contribution${orderedWork.length === 1 ? "" : "s"} with diminishing breadth weighting`,
      visuals[0] ? `Contextual lead visual selected from ${candidates.length} relevant evidence record${candidates.length === 1 ? "" : "s"}` : "No usable contextual lead visual is registered",
      documentationDepth ? "Documented stages or contextual summaries strengthen the projection" : "Contribution documentation is concise",
      `${leadHatSlugs.length} evidenced capabilities contribute a capped distinctiveness signal`,
      ...(directLensSupport ? ["Evidence is explicitly connected to the selected lens"] : []),
      ...(preference?.reason ? [`Editorial preference: ${preference.reason}`] : []),
    ];
    publicWorkCards.push({
      projectSlug: project.slug,
      ...(lensId ? { lensId } : {}),
      projectName: project.name,
      contributionTitle: orderedWork.length === 1 ? orderedWork[0].title : orderedWork.map((item) => item.title).join(" · "),
      summary,
      relevantWorkSlugs: orderedWork.map((item) => item.slug),
      leadHatSlugs: leadHatSlugs.slice(0, 4), supportingHatSlugs: leadHatSlugs.slice(4),
      ...(visuals[0] ? { primaryVisual: visuals[0] } : {}), supportingVisuals: visuals.slice(1),
      relevanceReasons,
      relevanceScore,
      editorialBoost,
      evidenceCompletenessScore: Math.round(evidenceCompleteness * 100) / 100,
      ...(Number.isFinite(preference?.editorialSequence) ? { editorialSequence: preference.editorialSequence } : {}),
      finalScore,
      href: lensId ? `/projects/${project.slug}?area=${lensId}` : `/projects/${project.slug}`,
    });
  }
}

